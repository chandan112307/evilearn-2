@app.get("/history", response_model=HistoryResponse)
def get_history():
    """Retrieve complete validation history — fully typed, no loose dicts."""
    raw_history = db.get_history()

    sessions: list[HistorySession] = []
    for s in raw_history:
        # Validate claims
        typed_claims = [
            HistoryClaimItem(
                claim_id=c.get("claim_id", ""),
                session_id=c.get("session_id", ""),
                claim_text=c.get("claim_text", ""),
            )
            for c in s.get("claims", [])
        ]

        # Validate results
        typed_results: list[ClaimResult] = []
        for r in s.get("results", []):
            evidence_items = [
                EvidenceItem(snippet=e.get("snippet", ""), page_number=e.get("page_number", 0))
                for e in r.get("evidence", [])
            ]
            typed_results.append(
                ClaimResult(
                    claim_id=r.get("claim_id", ""),
                    claim_text=r.get("claim_text", ""),
                    status=r.get("status", "unsupported"),
                    confidence_score=r.get("confidence_score", 0.0),
                    evidence=evidence_items,
                    explanation=r.get("explanation", ""),
                )
            )

        # Validate feedback
        typed_feedback = [
            HistoryFeedbackItem(
                feedback_id=f.get("feedback_id", ""),
                claim_id=f.get("claim_id", ""),
                session_id=f.get("session_id", ""),
                user_decision=f.get("user_decision", ""),
                created_at=f.get("created_at", ""),
            )
            for f in s.get("feedback", [])
        ]

        sessions.append(
            HistorySession(
                session_id=s.get("session_id", ""),
                input_text=s.get("input_text", ""),
                input_type=s.get("input_type"),
                created_at=s.get("created_at", ""),
                claims=typed_claims,
                results=typed_results,
                feedback=typed_feedback,
            )
        )

    return HistoryResponse(sessions=sessions)
