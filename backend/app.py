# --- Validation Endpoints ---

@app.post("/process-input", response_model=ProcessInputResponse)
def process_input(request: ProcessInputRequest):
    """Process user input through the validation pipeline.

    Executes: Input → Claims → Retrieval → Verification → Explanation → Output
    All output is validated via Pydantic BEFORE storage.
    """
    # Check if documents exist
    documents = db.get_documents()
    ready_docs = [d for d in documents if d.get("status") == "ready"]
    if not ready_docs:
        raise HTTPException(
            status_code=400,
            detail="No knowledge base available. Please upload documents first.",
        )

    try:
        # Execute pipeline
        result = pipeline.execute(request.input_text)

        # Create session
        session_id = db.create_session(
            input_text=request.input_text,
            input_type=result.get("input_type", "answer"),
        )

        claims = result.get("claims", [])

        if not claims:
            return ProcessInputResponse(
                session_id=session_id,
                input_type=result.get("input_type", "answer"),
                claims=[],
                message=result.get("message", "No claims extracted."),
            )

        # Validate ALL claims via ClaimResult Pydantic model BEFORE storage
        claim_results: list[ClaimResult] = []
        for c in claims:
            try:
                evidence_items = [
                    EvidenceItem(
                        snippet=e.get("snippet", ""),
                        page_number=e.get("page_number", 0),
                    )
                    for e in c.get("evidence", [])
                ]
                validated_claim = ClaimResult(
                    claim_id=c["claim_id"],
                    claim_text=c["claim_text"],
                    status=c.get("status", "unsupported"),
                    confidence_score=c.get("confidence_score", 0.0),
                    evidence=evidence_items,
                    explanation=c.get("explanation", ""),
                )
                claim_results.append(validated_claim)
            except ValidationError as ve:
                raise HTTPException(
                    status_code=500,
                    detail=f"Pipeline output validation failed for claim {c.get('claim_id', '?')}: {ve}",
                )

        # Store ONLY validated data
        db.insert_claims(
            session_id=session_id,
            claims=[
                {"claim_id": cr.claim_id, "claim_text": cr.claim_text}
                for cr in claim_results
            ],
        )

        db.insert_results(
            session_id=session_id,
            results=[cr.model_dump() for cr in claim_results],
        )

        return ProcessInputResponse(
            session_id=session_id,
            input_type=result.get("input_type", "answer"),
            claims=claim_results,
        )

    except ValueError as e:
        raise HTTPException(status_code=400, detail=str(e))
    except RuntimeError as e:
        raise HTTPException(status_code=500, detail=str(e))


# --- Stress Test Endpoint ---

@app.post("/evaluate-reasoning", response_model=EvaluateReasoningResponse)
def evaluate_reasoning(request: EvaluateReasoningRequest):
    """Stress-test student reasoning to expose weaknesses and fragility.

    This is NOT a tutoring endpoint. It actively tries to break the
    student's reasoning by generating adversarial scenarios, detecting
    failure points, and producing targeted challenge questions.

    Executes: Input → Claims → Retrieval → Verification → Stress Test
    """
    try:
        result = pipeline.evaluate_reasoning(
            student_answer=request.student_answer,
            problem=request.problem,
        )

        # Validate output via Pydantic
        weakness_items = [
            WeaknessItem(type=w.get("type", ""), detail=w.get("detail", ""))
            for w in result.get("weakness_summary", [])
        ]

        robustness_raw = result.get("robustness_summary", {})
        robustness = RobustnessSummary(
            robustness_score=robustness_raw.get("robustness_score", 0.0),
            summary=robustness_raw.get("summary", ""),
            level=robustness_raw.get("level", "unknown"),
        )

        return EvaluateReasoningResponse(
            stress_test_results=result.get("stress_test_results", []),
            weakness_summary=weakness_items,
            robustness_summary=robustness,
            adversarial_questions=result.get("adversarial_questions", []),
        )

    except ValueError as e:
        raise HTTPException(status_code=400, detail=str(e))
    except RuntimeError as e:
        raise HTTPException(status_code=500, detail=str(e))


